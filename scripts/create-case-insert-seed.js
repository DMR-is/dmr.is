const generateSignatureSeed = ({
  id,
  type_id,
  date,
  institution,
  involved_party_id,
  chairman_id,
  members,
  additonal_signature,
  html,
  caseId,
}) => {
  const signatureId = id ? id : v4()
  let dump = ''

  members.forEach((member) => {
    dump += `
        INSERT INTO
          SIGNATURE_MEMBER (
            ID,
            VALUE,
            TEXT_ABOVE,
            TEXT_BELOW,
            TEXT_AFTER
          )
          VALUES (
            '${member.id}',
            '${member.value}',
            ${member.text_above ? `'${member.text_above}'` : `NULL`},
            ${member.text_below ? `'${member.text_below}'` : `NULL`},
            ${member.text_after ? `'${member.text_after}'` : `NULL`}
          );
        `
  })

  dump += `
  INSERT INTO
    SIGNATURE (
      ID,
      TYPE_ID,
      DATE,
      INSTITUTION,
      INVOLVED_PARTY_ID,
      CHAIRMAN_ID,
      ADDITIONAL_SIGNATURE,
      HTML
    )
    VALUES (
      '${signatureId}',
      '${type_id}',
      '${date}',
      '${institution}',
      '${involved_party_id}',
      ${chairman_id ? `'${chairman_id}'` : `NULL`},
      ${additonal_signature ? `'${additonal_signature}'` : `NULL`},
      '${html}'
    );
  `

  members.forEach((member) => {
    dump += `
    INSERT INTO
      SIGNATURE_MEMBERS (
        SIGNATURE_ID,
        SIGNATURE_MEMBER_ID
      )
      VALUES (
        '${signatureId}',
        '${member.id}'
      );
    `
  })

  dump += `
  INSERT INTO
    CASE_SIGNATURES (
      CASE_CASE_ID,
      SIGNATURE_ID
    )
    VALUES (
      '${caseId}',
      '${signatureId}'
    );
  `

  return dump
}

