const findTypeFromDepartmentId = (departmentId) => {
  let found = false
  while (!found) {
    const type = TYPE_IDS[Math.floor(Math.random() * TYPE_IDS.length)]
    if (type.departmentId === departmentId) {
      found = true
      return type
    }
  }
}

const getRandomDate = (minYear = 2022) => {
  const start = new Date(`${minYear}-01-01`)
  const end = new Date('2024-07-23')

  return new Date(
    start.getTime() + Math.random() * (end.getTime() - start.getTime()),
  )
}

const getRequestedPublicationDate = (createdAt) => {
  const startDate = new Date(createdAt)
  const endDate = new Date(createdAt)
  endDate.setDate(endDate.getDate() + 30)
  const randomDate = new Date(
    startDate.getTime() +
      Math.random() * (endDate.getTime() - startDate.getTime()),
  )
  return randomDate
}
const addLeadingZeros = (number, max = 2) => {
  const zeros = Array(max).fill('0').join('')
  return (zeros + number).slice(-max)
}

const getCaseNumber = (date) => {
  const year = date.getFullYear()
  const month = addLeadingZeros(date.getMonth() + 1)
  const day = addLeadingZeros(date.getDate())

  return `${year}${addLeadingZeros(month, 2)}${addLeadingZeros(day, 3)}`
}

const pickRandom = (arr) => {
  return arr[Math.floor(Math.random() * arr.length)]
}

/**
 * Generates random case seed dump under root/tmp/case-insert-dump.sql
 * accepts number of iterations as argument
 */
function main() {
  try {
    const iterations =
      typeof process.argv[2] === 'string' ? parseInt(process.argv[2]) : 20

    if (isNaN(iterations)) {
      throw new Error('Invalid argument, please provide a number')
    }

    let insertSeed = ``

    for (let index = 0; index < iterations; index++) {
      const id = v4()

      const departmentId = pickRandom(DEPARTMENT_IDS)

      const typeId = findTypeFromDepartmentId(departmentId)

      const year = Math.floor(Math.random() * 2) + 2022
      const created = getRandomDate(year)

      const caseNumber = getCaseNumber(created)
      const statusId = pickRandom(STATUS_IDS)
      const tagId = pickRandom(TAG_IDS)
      const isLegacy = Math.random() > 0.5
      const involvedPartyId = pickRandom(INVOLVED_PARTY_IDS)

      const communicationStatusId = pickRandom(COMMUNICATION_STATUS_IDS)
      const paid = Math.random() > 0.5

      const requestPublicationDate = getRequestedPublicationDate(created)

      // if requested publication date is in next 10 days from created date
      const fastTrack =
        requestPublicationDate.getTime() - created.getTime() < 10 * 24 * 60 * 60

      const title = pickRandom(ADVERT_TITLES)

      const assignedUserId =
        statusId === SUBMITTED_STATUS ? undefined : pickRandom(EMPLOYEE_IDS)

      const caseDump = generateCaseInsertSeed({
        id,
        applicationId: APPLICATION_ID,
        year,
        caseNumber,
        statusId,
        tagId,
        involvedPartyId,
        createdAt: created.toISOString(),
        updatedAt: created.toISOString(),
        isLegacy,
        assignedUserId: assignedUserId,
        caseCommunicationStatusId: communicationStatusId,
        paid,
        fastTrack,
        departmentId,
        advertTypeId: typeId.typeId,
        advertTitle: title,
        advertRequestedPublicationDate: requestPublicationDate.toISOString(),
      })

      const categoryCount = Math.floor(Math.random() * 6)
      const categoryIds = Array.from({ length: categoryCount }, () =>
        pickRandom(CATEGORY_IDS),
      )

      const uniqueCategories = Array.from(new Set(categoryIds))

      const categoryDump = uniqueCategories
        .map((categoryId) => generateCaseCategories({ caseId: id, categoryId }))
        .join('\n')

      // signatures
      let signatureDump = ''
      const signatureTypeId =
        Math.random() > 0.5 ? REGULAR_SIGNATURE_ID : COMMITTEE_SIGNATURE_ID

      const numberOfSignatures =
        signatureTypeId === REGULAR_SIGNATURE_ID
          ? Math.floor(Math.random() * 3) + 1
          : 1

      Array.from({ length: numberOfSignatures }, () => {
        const numberOfSignatureMembers = Math.floor(Math.random() * 5) + 1
        const members = []
        Array.from({ length: numberOfSignatureMembers }, () => {
          const isMale = Math.random() > 0.5
          const name = getRandomName(isMale)
          const text = pickRandom(TITLES)
          const textPlacementRand = Math.random()
          const textPlacement =
            textPlacementRand > 0.33
              ? 'right'
              : textPlacementRand > 0.66
              ? 'above'
              : 'below'

          members.push({
            id: v4(),
            value: name,
            text_above: textPlacement === 'above' ? text : null,
            text_below: textPlacement === 'below' ? text : null,
            text_after: textPlacement === 'right' ? text : null,
          })
        })

        signatureDump += generateSignatureSeed({
          type_id: signatureTypeId,
          date: created.toISOString(),
          institution: pickRandom(INSTITUTIONS),
          involved_party_id: involvedPartyId,
          chairman_id:
            signatureTypeId === COMMITTEE_SIGNATURE_ID ? members[0].id : null,
          members,
          caseId: id,
          html: `<div>Signature</div>`,
        })
      })

      insertSeed += caseDump
      insertSeed += categoryDump
      insertSeed += signatureDump
    }

    const filePath = path.join(__dirname, '../tmp/case-insert-dump.sql')
    fs.writeFileSync(filePath, insertSeed)
    console.log(
      `Generated ${iterations} case insert seeds, stored at ${filePath}`,
    )
  } catch (error) {
    console.error(error)
    console.log('Error generating case insert seed')
  }
}

main()
