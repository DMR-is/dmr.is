const ROOT_SQL_DIR = './apps/official-journal-api-export/sql'

function log(message: string) {
  // eslint-disable-next-line no-console
  console.log(message)
}

function write(path: string, data: string) {
  const joinedPath = join(ROOT_SQL_DIR, path)
  return writeFile(joinedPath, data, { flag: 'w+' })
}

async function exec<T = unknown>(
  action: string,
  fn: () => Promise<T>,
  emoji = '⏰',
) {
  log(`${emoji} starting ${action}`)
  const start = hrtime.bigint()
  const result = await fn()
  const end = hrtime.bigint()

  const elapsedInSec = (Number(end - start) / 1e9).toFixed(2)

  const lengtString = Array.isArray(result) ? ` (${result.length} records)` : ''

  log(`${emoji} finished ${action} in ${elapsedInSec} seconds${lengtString}`)
  return result
}

type SQLInsert = {
  NationalID: string
  FirstName: string
  LastName: string
  Email: string
  Department: string
  DepartmentEmail: string
  DepartmentID: string
  Role: string
}

async function exportFromCsvToSQL() {
  //read from local csv file
  const records: any[] = []
  const parser = createReadStream('./all-new.csv').pipe(
    parse({ columns: true, skip_empty_lines: true }),
  )

  for await (const record of parser) {
    records.push(record)
  }
  // Group by a field, e.g., 'category'
  const grouped = records.reduce(
    (acc, record) => {
      const key = record.Department
      if (!acc[key]) {
        acc[key] = []
      }
      acc[key].push(record)
      return acc
    },
    {} as Record<string, SQLInsert[]>,
  )

  //create SQL inserts per each group
  let departmentString = ''
  const sqlInserts = Object.entries(grouped).map(([key, records]) => {
    //insert for department
    /*BEGIN TRANSACTION;
insert into api_scope_user(national_id,email,name) values ('7003051040','gadget@hugsmidjan.is','GADGET ER HÆTT ehf.');
insert into api_scope_user_access(national_id,scope) values ('7003051040','@island.is/applications/dmr:stjornartidindi');
COMMIT;*/
    const departmentId = (records as SQLInsert[])[0].DepartmentID
    const departmentName = (records as SQLInsert[])[0].Department
    const departmentEmail = (records as SQLInsert[])[0].DepartmentEmail
    const apiScopeString = `insert into api_scope_user(national_id,email,name) values ('${departmentId}','${departmentEmail}','${departmentName}') ON CONFLICT (national_id) DO NOTHING;`
    const apiScopeAccessString = `insert into api_scope_user_access(national_id,scope) values ('${departmentId}','@island.is/applications/dmr:stjornartidindi');`
    const values = (records as Array<SQLInsert>).map((record) => {
      const delegationString = `insert into delegation (id,from_national_id,from_display_name,to_national_id,to_name,domain_name) values (gen_random_uuid(),'${record.DepartmentID}','${record.Department}','${record.NationalID}','${record.FirstName} ${record.LastName}','@island.is') RETURNING id INTO delegation_id_return;`
      const delegationScopeNormal = `insert into delegation_scope(delegation_id,scope_name,valid_from,valid_to,id) values (delegation_id_return,'@island.is/applications/dmr:stjornartidindi','2025-05-22 00:00:00+00','2026-05-22 00:00:00+00',gen_random_uuid()) ON CONFLICT DO NOTHING;`
      const delegationScopeWrite = `insert into delegation_scope(delegation_id,scope_name,valid_from,valid_to,id) values (delegation_id_return,'@island.is/auth/delegations:write','2025-05-22 00:00:00+00','2026-05-22 00:00:00+00',gen_random_uuid());`

      const returnString = `DO $$\nDECLARE delegation_id_return DELEGATION.id%TYPE;\nBEGIN\n${delegationString}\n${delegationScopeNormal}\n${record.Role === 'Fulltrúi' ? delegationScopeWrite : ''}\nEND $$;\n\n`
      return returnString
    })
    departmentString += `${apiScopeString}\n${apiScopeAccessString}\n\n`
    return `${values.filter((v) => v !== undefined).join('\n')}`
  })

  //Write sqlInsert to file

  const sqlFileContent = sqlInserts.join('\n')
  await write('allScopeUsers.sql', sqlFileContent)
  await write(
    'allScopeDepartments.sql',
    `BEGIN TRANSACTION \n ${departmentString}\n ROLLBACK;`,
  )
  log(`SQL inserts written to allScope.sql`)
}

